#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, '../../..');
const STATUS_PATH = path.join(ROOT, 'doc/04_assets/ui_suite_gpt_v1/specs/pixel-perfect-pipeline-status.json');

function repoPath(file) {
  return path.isAbsolute(file) ? file : path.join(ROOT, file);
}

function repoRel(file) {
  return path.relative(ROOT, file).replaceAll(path.sep, '/');
}

function readJson(file, fallback = null) {
  const abs = repoPath(file);
  if (!fs.existsSync(abs)) return fallback;
  return JSON.parse(fs.readFileSync(abs, 'utf8'));
}

function writeJson(file, value) {
  const abs = repoPath(file);
  fs.mkdirSync(path.dirname(abs), { recursive: true });
  fs.writeFileSync(abs, `${JSON.stringify(value, null, 2)}\n`);
}

function appendReviewNote(reviewPath, batchReason) {
  const abs = repoPath(reviewPath);
  if (!fs.existsSync(abs)) return false;
  const current = fs.readFileSync(abs, 'utf8');
  if (current.includes('## Corrected Auxiliary Review Gate')) return false;
  const note = `

## Corrected Auxiliary Review Gate

- Main-thread correction: ${batchReason}
- The script-written auxiliary review line is treated as evidence metadata only.
- Final page acceptance requires an independent subagent review batch plus a main-thread application step.
`;
  fs.writeFileSync(abs, `${current.trimEnd()}\n${note.trimEnd()}\n`);
  return true;
}

function main() {
  const status = readJson(STATUS_PATH, { items: [] });
  const demoted = [];
  const skipped = [];
  for (const item of status.items || []) {
    if (item.category !== 'pages' || item.stage !== 'pixel-accepted') continue;
    const record = readJson(item.files?.json, null);
    if (!record) continue;
    const verificationPath = record.evidence?.verification || item.files?.verification;
    const verification = readJson(verificationPath, null);
    const auxAgent = verification?.auxiliary_agent_review?.agent || '';
    const hasIndependentReview = Boolean(verification?.independent_subagent_review?.review_batch);
    if (hasIndependentReview) {
      skipped.push({ id: item.id, reason: 'already has independent_subagent_review' });
      continue;
    }
    if (auxAgent && auxAgent !== 'pixel-breakdown-reviewer') {
      skipped.push({ id: item.id, reason: `auxiliary agent is ${auxAgent}` });
      continue;
    }

    record.status = 'evidence-ready';
    record.accepted = false;
    record.review_gate = {
      status: 'requires-independent-subagent-review',
      reason: 'Pages generated by the earlier loop must not use script-written auxiliary review as final acceptance.',
    };
    writeJson(item.files.json, record);

    if (verification) {
      verification.status = 'evidence-ready';
      verification.accepted = false;
      verification.main_thread_judgment = 'awaiting-real-auxiliary-review';
      verification.main_thread_decision_basis =
        'Evidence exists, but page-level final acceptance is withheld until an independent subagent review batch is applied by the main thread.';
      verification.auxiliary_agent_review = {
        agent: 'independent-subagent-required',
        status: 'requested',
        notes: [
          'The earlier script-written auxiliary review is not used as final page acceptance.',
          'A separate subagent must inspect target, implementation screenshot, diff, metrics, overlay, record, and capture metadata.',
        ],
      };
      verification.independent_subagent_review = {
        status: 'required',
        review_batch: '',
      };
      writeJson(verificationPath, verification);
    }
    appendReviewNote(item.files.review, 'page acceptance gate moved to independent subagent review');
    demoted.push({
      id: item.id,
      category: item.category,
      record: item.files.json,
      verification: verificationPath,
      review: item.files.review,
    });
  }
  console.log(JSON.stringify({ demoted_count: demoted.length, skipped_count: skipped.length, demoted, skipped }, null, 2));
}

main();
