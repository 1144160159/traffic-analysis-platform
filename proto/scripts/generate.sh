# =============================================================================
# 主函数
# =============================================================================

main() {
    echo ""
    echo "=============================================================="
    echo "     Traffic Analysis Platform - Protobuf Code Generator"
    echo "=============================================================="
    echo ""
    
    local start_time
    start_time=$(date +%s)
    
    # 解析参数
    local skip_clean=false
    local skip_lint=false
    local skip_verify=false
    
    while [[ $# -gt 0 ]]; do
        case $1 in
            --skip-clean)
                skip_clean=true
                shift
                ;;
            --skip-lint)
                skip_lint=true
                shift
                ;;
            --skip-verify)
                skip_verify=true
                shift
                ;;
            --help|-h)
                echo "Usage: $0 [OPTIONS]"
                echo ""
                echo "Options:"
                echo "  --skip-clean    Skip cleaning old generated files"
                echo "  --skip-lint     Skip proto linting"
                echo "  --skip-verify   Skip verification"
                echo "  --help, -h      Show this help message"
                exit 0
                ;;
            *)
                log_error "Unknown option: $1"
                exit 1
                ;;
        esac
    done
    
    # 执行步骤
    check_dependencies
    
    if [[ "$skip_clean" != true ]]; then
        clean_generated
    fi
    
    create_output_dirs
    
    if [[ "$skip_lint" != true ]]; then
        lint_protos
    fi
    
    generate_code
    generate_rust_lib
    
    if [[ "$skip_verify" != true ]]; then
        verify_all
    fi
    
    # 可选: 编译测试
    # test_rust_compile
    # test_go_compile
    
    local end_time
    end_time=$(date +%s)
    local duration=$((end_time - start_time))
    
    echo ""
    log_success "All tasks completed in ${duration}s"
    
    print_integration_guide
}

# 执行主函数
main "$@"