#!/usr/bin/env bash
set -euo pipefail

# =========================
# Protobuf 代码生成脚本
# =========================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROTO_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"
ROOT_DIR="$(cd "${PROTO_DIR}/.." && pwd)"

# 颜色输出
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

log_info() {
    echo -e "${GREEN}[INFO]${NC} $1"
}

log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# =========================
# 1. 检查依赖
# =========================
check_dependencies() {
    log_info "Checking dependencies..."

    # 检查 buf
    if ! command -v buf &> /dev/null; then
        log_error "buf is not installed. Please install it:"
        echo "  https://docs.buf.build/installation"
        exit 1
    fi

    BUF_VERSION=$(buf --version | awk '{print $2}')
    log_info "buf version: ${BUF_VERSION}"

    # 检查输出目录是否存在
    mkdir -p "${ROOT_DIR}/rust/probe-agent/proto-gen/src"
    mkdir -p "${ROOT_DIR}/go/control-plane/pkg/proto"
    mkdir -p "${ROOT_DIR}/java/flink-jobs/flink-common/src/main/java"
}

# =========================
# 2. 清理旧文件
# =========================
clean_old_files() {
    log_info "Cleaning old generated files..."

    # Rust
    if [ -d "${ROOT_DIR}/rust/probe-agent/proto-gen/src" ]; then
        find "${ROOT_DIR}/rust/probe-agent/proto-gen/src" -type f -name "*.rs" ! -name "lib.rs" -delete
    fi

    # Go
    if [ -d "${ROOT_DIR}/go/control-plane/pkg/proto" ]; then
        find "${ROOT_DIR}/go/control-plane/pkg/proto" -type f \( -name "*.pb.go" -o -name "*_grpc.pb.go" \) -delete
    fi

    # Java
    if [ -d "${ROOT_DIR}/java/flink-jobs/flink-common/src/main/java/com/traffic/proto" ]; then
        rm -rf "${ROOT_DIR}/java/flink-jobs/flink-common/src/main/java/com/traffic/proto"
    fi
}

# =========================
# 3. 验证 Proto 文件
# =========================
lint_protos() {
    log_info "Linting proto files..."
    cd "${PROTO_DIR}"
    
    if ! buf lint; then
        log_error "Proto lint failed!"
        exit 1
    fi
    
    log_info "✓ Lint passed"
}

# =========================
# 4. 生成代码
# =========================
generate_code() {
    log_info "Generating code for Rust, Go, and Java..."
    cd "${PROTO_DIR}"

    if ! buf generate; then
        log_error "Code generation failed!"
        exit 1
    fi

    log_info "✓ Code generation completed"
}

# =========================
# 5. 后处理：Rust lib.rs
# =========================
post_process_rust() {
    log_info "Post-processing Rust code..."

    RUST_PROTO_DIR="${ROOT_DIR}/rust/probe-agent/proto-gen/src"
    LIB_RS="${RUST_PROTO_DIR}/lib.rs"

    # 生成 lib.rs 文件
    cat > "${LIB_RS}" <<'EOF'
// Auto-generated by proto/scripts/generate.sh
// DO NOT EDIT MANUALLY

#![allow(clippy::all)]
#![allow(warnings)]

pub mod traffic {
    pub mod v1 {
        include!("traffic.v1.rs");
    }
}

// Re-export commonly used types
pub use traffic::v1::*;
EOF

    log_info "✓ Rust lib.rs generated"
}

# =========================
# 6. 后处理：Go go.mod 检查
# =========================
post_process_go() {
    log_info "Post-processing Go code..."

    GO_MOD="${ROOT_DIR}/go/control-plane/go.mod"

    if [ ! -f "${GO_MOD}" ]; then
        log_warn "go.mod not found, creating..."
        cd "${ROOT_DIR}/go/control-plane"
        go mod init github.com/1144160159/traffic-analysis-platform/go/control-plane
    fi

    cd "${ROOT_DIR}/go/control-plane"
    go mod tidy || log_warn "go mod tidy failed (may need to run manually after implementing services)"

    log_info "✓ Go modules updated"
}

# =========================
# 7. 后处理：Java pom.xml 检查
# =========================
post_process_java() {
    log_info "Post-processing Java code..."

    JAVA_POM="${ROOT_DIR}/java/flink-jobs/flink-common/pom.xml"

    if [ ! -f "${JAVA_POM}" ]; then
        log_warn "flink-common/pom.xml not found. Please ensure protobuf dependencies are added."
    else
        log_info "✓ Java structure verified"
    fi
}

# =========================
# 8. 验证生成结果
# =========================
verify_output() {
    log_info "Verifying generated files..."

    local failed=0

    # Rust
    if [ ! -f "${ROOT_DIR}/rust/probe-agent/proto-gen/src/lib.rs" ]; then
        log_error "Rust lib.rs not found"
        failed=1
    fi

    if [ ! -f "${ROOT_DIR}/rust/probe-agent/proto-gen/src/traffic.v1.rs" ]; then
        log_error "Rust traffic.v1.rs not found"
        failed=1
    fi

    # Go
    if ! find "${ROOT_DIR}/go/control-plane/pkg/proto" -name "*.pb.go" | grep -q .; then
        log_error "No Go .pb.go files found"
        failed=1
    fi

    # Java
    if ! find "${ROOT_DIR}/java/flink-jobs/flink-common/src/main/java" -name "*.java" | grep -q .; then
        log_error "No Java files found"
        failed=1
    fi

    if [ ${failed} -eq 1 ]; then
        log_error "Verification failed!"
        exit 1
    fi

    log_info "✓ All files verified"
}

# =========================
# 9. 主流程
# =========================
main() {
    log_info "========================================="
    log_info " Traffic Analysis Platform - Proto Gen"
    log_info "========================================="

    check_dependencies
    clean_old_files
    #lint_protos
    generate_code
    post_process_rust
    post_process_go
    post_process_java
    verify_output

    log_info ""
    log_info "========================================="
    log_info "✓ Code generation completed successfully!"
    log_info "========================================="
    log_info ""
    log_info "Generated files:"
    log_info "  Rust:  ${ROOT_DIR}/rust/probe-agent/proto-gen/src/"
    log_info "  Go:    ${ROOT_DIR}/go/control-plane/pkg/proto/"
    log_info "  Java:  ${ROOT_DIR}/java/flink-jobs/flink-common/src/main/java/"
    log_info ""
    log_info "Next steps:"
    log_info "  1. Commit generated files to Git"
    log_info "  2. Run 'cargo build' in rust/probe-agent/"
    log_info "  3. Run 'go mod tidy' in go/control-plane/"
    log_info "  4. Run 'mvn compile' in java/flink-jobs/"
}

main "$@"
